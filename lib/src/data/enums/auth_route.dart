/// presents the diffrent auth routes.
enum AuthRoute {
  /// use welcome route
  welcome,

  /// use signin route
  signin,

  /// use signup route
  register,

  /// use forgetpassword route
  forgotpassword,

  /// use verify OTP for password route
  passwordverifyotp,

  /// use verify OTP for register route
  registerverifyotp,

  /// use reset password route
  resetpassword,
}
